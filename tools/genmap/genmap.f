      subroutine breadth_first_fill(p,np)
      integer p(0:1)
      integer start,stride,count

      kmax     = log2(np) + 1
      count    = 0
      p(count) = 0
      stride   = np

      do k=1,kmax
         next = stride/2
         do i=1,np
            count    = count+1
            p(count) = next
            next     = next + stride
            if (next.ge.np) goto 10
         enddo
   10    continue
         stride = stride/2
      enddo

      return
      end
