c        elseif (wk(0,i).lt.dx(0,ig)) then
c           call copy(dx(0,ig),wk(0,i),lda) ! lda = ndim+1 values ->global vtx
c        endif
c     enddo
      write(6,6) nseg,nglb,n,icm
    6 format(' done locglob_lexico:',4i12)


      return
      end
